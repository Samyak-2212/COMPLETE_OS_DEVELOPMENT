/* ── Framebuffer API implementation ─────────────────────────────────────── */

/* Initialize the framebuffer from Limine response. */
int framebuffer_init(void) {
    struct limine_framebuffer_response *resp = limine_get_framebuffer();
    if (resp == (void *)0 || resp->framebuffer_count < 1) {
        return -1;
    }

    struct limine_framebuffer *fb = resp->framebuffers[0];
    fb_info.address = (volatile uint32_t *)fb->address;
    fb_info.width   = fb->width;
    fb_info.height  = fb->height;
    fb_info.pitch   = fb->pitch;
    fb_info.bpp     = fb->bpp;

    fb_initialized = 1;
    return 0;
}

/* Get framebuffer information */
const framebuffer_info_t *framebuffer_get_info(void) {
    return &fb_info;
}

/* Put a single pixel at (x, y) with 32-bit ARGB color */
void framebuffer_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (!fb_initialized) return;
    if (x >= fb_info.width || y >= fb_info.height) return;
    /* pitch is in bytes; we're using uint32_t pointers → divide by 4 */
    fb_info.address[y * (fb_info.pitch / 4) + x] = color;
}

/* Fill the entire screen with a solid color */
void framebuffer_clear(uint32_t color) {
    if (!fb_initialized) return;
    for (uint64_t y = 0; y < fb_info.height; y++) {
        for (uint64_t x = 0; x < fb_info.width; x++) {
            fb_info.address[y * (fb_info.pitch / 4) + x] = color;
        }
    }
}

/* Scroll the framebuffer up by font_height pixels */
void framebuffer_scroll(uint32_t font_height) {
    if (!fb_initialized) return;
    uint32_t pixels_per_row = (uint32_t)(fb_info.pitch / 4);
    uint32_t rows_to_copy = (uint32_t)(fb_info.height - font_height);

    /* Move everything up by font_height rows */
    for (uint32_t y = 0; y < rows_to_copy; y++) {
        volatile uint32_t *dst = fb_info.address + y * pixels_per_row;
        volatile uint32_t *src = fb_info.address + (y + font_height) * pixels_per_row;
        for (uint32_t x = 0; x < fb_info.width; x++) {
            dst[x] = src[x];
        }
    }

    /* Clear the bottom font_height rows with default navy blue */
    for (uint32_t y = rows_to_copy; y < (uint32_t)fb_info.height; y++) {
        for (uint32_t x = 0; x < fb_info.width; x++) {
            fb_info.address[y * pixels_per_row + x] = 0x001A1A2E;
        }
    }
}

/* Draw a single character glyph at pixel position (px, py) */
void framebuffer_draw_char(uint32_t px, uint32_t py, char c,
                           uint32_t fg, uint32_t bg) {
    if (!fb_initialized) return;
    uint8_t uc = (uint8_t)c;
    if (uc > 0x7E) uc = '?'; /* Replace non-printable with '?' */

    const uint8_t *glyph = vga_font[uc];
    uint32_t pixels_per_row = (uint32_t)(fb_info.pitch / 4);

    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            uint32_t x = px + col;
            uint32_t y = py + row;
            if (x >= fb_info.width || y >= fb_info.height) continue;
            /* MSB is leftmost pixel */
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_info.address[y * pixels_per_row + x] = color;
        }
    }
}

/* Get terminal grid dimensions based on the built-in font */
void framebuffer_get_text_dimensions(uint32_t *cols, uint32_t *rows) {
    if (cols) *cols = (uint32_t)(fb_info.width / FONT_WIDTH);
    if (rows) *rows = (uint32_t)(fb_info.height / FONT_HEIGHT);
}
